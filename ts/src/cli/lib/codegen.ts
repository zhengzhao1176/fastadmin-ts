// Pure-function code generators for `think crud`. Each function takes a
// TableInfo and returns the file contents as a string. Output mirrors the
// Category controller pattern that's already test-verified.
import type { TableInfo, ColumnInfo } from './schema-introspect.ts'

/** Convert snake_case / fa_snake_case to PascalCase. */
export function pascalCase(s: string): string {
  return s.replace(/^fa_/, '').replace(/(?:^|_)(\w)/g, (_, c) => (c as string).toUpperCase())
}

/** Convert snake_case to camelCase, drop fa_ prefix. */
export function shortName(s: string): string {
  return s.replace(/^fa_/, '')
}

/**
 * FastAdmin's framework / core tables — `php think crud` must NOT scaffold
 * these (their controllers are hand-written and security-sensitive, and a
 * generated controller would collide with the existing one). Mirrors the
 * doc's explicit warning against running CRUD on core tables.
 */
const PROTECTED_TABLES = new Set([
  'admin', 'admin_log', 'auth_group', 'auth_group_access', 'auth_rule',
  'config', 'attachment', 'user', 'user_group', 'user_rule', 'user_token',
  'user_money_log', 'user_score_log', 'ems', 'sms', 'area', 'category', 'token',
])

/** True when `table` is a framework/core table CRUD generation must refuse. */
export function isProtectedTable(table: string): boolean {
  return PROTECTED_TABLES.has(String(table).toLowerCase().replace(/^fa_/, ''))
}

function tsTypeForColumn(c: ColumnInfo): string {
  if (c.dataType === 'tinyint' && c.columnType === 'tinyint(1)') return 'boolean'
  if (['int', 'bigint', 'smallint', 'mediumint', 'tinyint', 'decimal', 'float', 'double'].includes(c.dataType)) return 'number'
  if (c.enumValues) return 'string'
  return 'string'
}

// TypeORM only accepts `length` on string-family columns. enum / set / text /
// numeric types throw "Column X does not support length property" if it's
// present. `set` is mapped to `varchar` below so it keeps its length.
const LENGTH_SUPPORTED = new Set(['varchar', 'char', 'set', 'varbinary', 'binary'])

function typeormTypeForColumn(c: ColumnInfo): { type: string; extra: string } {
  const opts: string[] = []
  if (c.unsigned) opts.push('unsigned: true')
  if (c.length && LENGTH_SUPPORTED.has(c.dataType)) opts.push(`length: ${c.length}`)
  if (c.nullable) opts.push('nullable: true')
  if (c.default !== null && c.default !== undefined && c.default !== '') {
    opts.push(`default: ${JSON.stringify(c.default)}`)
  } else if (c.default === '') {
    opts.push(`default: ''`)
  }
  if (c.comment) opts.push(`comment: ${JSON.stringify(c.comment)}`)

  const extra = opts.length > 0 ? ', ' + opts.join(', ') : ''

  // Direct MySQL→TypeORM type map; bigints come through as strings from mysql2
  // so we add a transformer.
  if (c.dataType === 'bigint') {
    return {
      type: 'bigint',
      extra: `, transformer: { from: (v: string | null) => v == null ? null : Number(v), to: (v: number | null) => v }${extra}`,
    }
  }
  if (c.dataType === 'enum' && c.enumValues) {
    return { type: 'enum', extra: `, enum: ${JSON.stringify(c.enumValues)}${extra}` }
  }
  if (c.dataType === 'set') {
    // Set columns store as comma-joined string at the wire level; keep as varchar.
    return { type: 'varchar', extra }
  }
  return { type: c.dataType, extra }
}

export function generateEntity(table: TableInfo): string {
  const Class = pascalCase(table.name) + 'Entity'
  const lines: string[] = []
  lines.push(`// Auto-generated by \`bin/think crud -t ${table.name}\`. Edit freely.`)
  lines.push(`import { Column, Entity, PrimaryGeneratedColumn, PrimaryColumn } from 'typeorm'`)
  lines.push('')
  lines.push(`@Entity({ name: '${table.name}' })`)
  lines.push(`export class ${Class} {`)
  for (const col of table.columns) {
    const tsT = tsTypeForColumn(col)
    if (col.isPk && col.isAuto) {
      lines.push(`  @PrimaryGeneratedColumn({ type: '${col.dataType}' })`)
      lines.push(`  ${col.name}!: ${tsT}`)
    } else if (col.isPk) {
      const { type, extra } = typeormTypeForColumn(col)
      lines.push(`  @PrimaryColumn({ type: '${type}'${extra} })`)
      lines.push(`  ${col.name}!: ${tsT}`)
    } else {
      const { type, extra } = typeormTypeForColumn(col)
      const nullableTs = col.nullable ? ' | null' : ''
      lines.push(`  @Column({ type: '${type}'${extra} })`)
      lines.push(`  ${col.name}!: ${tsT}${nullableTs}`)
    }
    lines.push('')
  }
  lines.push(`}`)
  return lines.join('\n')
}

/**
 * Build the bootstrap-table `columns` literal for the list page. A CRUD-
 * generated controller has no hand-written `public/assets/js/backend/<x>.js`,
 * so the table is initialised inline (`useInlineInit: true`) and needs the
 * column set spelled out. Heavy columns (text / long varchar) are skipped to
 * keep the grid readable; an `operate` column is always appended.
 */
function buildColumnsLiteral(table: TableInfo): string {
  const cols: string[] = ['{ checkbox: true }']
  let shown = 0
  for (const c of table.columns) {
    if (shown >= 10) break
    if (c.dataType === 'text') continue
    if (c.dataType === 'varchar' && (c.length ?? 0) > 500) continue
    const title = pascalCase(c.name).replace(/([A-Z])/g, ' $1').trim()
    if (c.isPk) {
      cols.push(`{ field: '${c.name}', title: '${title}', sortable: true }`)
    } else {
      cols.push(`{ field: '${c.name}', title: '${title}' }`)
    }
    shown++
  }
  cols.push(`{ operate: true, title: 'Operate' }`)
  return '[\n        ' + cols.join(',\n        ') + ',\n      ]'
}

/**
 * Columns literal for the Recycle Bin grid. Like `buildColumnsLiteral` but
 * trimmed to ~6 data columns and always followed by a `deletetime` column
 * (datetime-formatted) so the operator can see when each row was binned. The
 * `operate` column is rebound to Restore / Destroy buttons by
 * `renderRecyclebinInitScript`.
 */
function buildRecyclebinColumnsLiteral(table: TableInfo): string {
  const cols: string[] = ['{ checkbox: true }']
  let shown = 0
  for (const c of table.columns) {
    if (shown >= 6) break
    if (c.dataType === 'text') continue
    if (c.dataType === 'varchar' && (c.length ?? 0) > 500) continue
    if (['createtime', 'updatetime', 'deletetime'].includes(c.name)) continue
    const title = pascalCase(c.name).replace(/([A-Z])/g, ' $1').trim()
    if (c.isPk) {
      cols.push(`{ field: '${c.name}', title: '${title}', sortable: true }`)
    } else {
      cols.push(`{ field: '${c.name}', title: '${title}' }`)
    }
    shown++
  }
  cols.push(`{ field: 'deletetime', title: 'Delete Time', formatter: 'Table.api.formatter.datetime' }`)
  cols.push(`{ operate: true, title: 'Operate' }`)
  return '[\n        ' + cols.join(',\n        ') + ',\n      ]'
}

