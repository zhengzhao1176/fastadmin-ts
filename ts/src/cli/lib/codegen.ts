function renderFieldsLiteral(table: TableInfo): string {
  // Generate a JS template literal that produces the <input> markup for each
  // non-pk, non-timestamp column. The widget chosen for each column follows
  // the FastAdmin field→component rules documented in database.html +
  // docs 177-186, 736 (upload / SelectPage / validator / city / datetime /
  // switch / slider / tagsinput / daterange):
  //   - category_id(s) / user_id(s) / admin_id(s) → SelectPage   (doc 178)
  //   - `city`                                     → city-picker (doc 180)
  //   - date / datetime / timestamp / *time        → datetimepicker (doc 181)
  //   - *range                                     → datetimerange (doc 736)
  //   - `switch` / tinyint(1)                       → switcher    (doc 185)
  //   - `tags` / *tags                              → tagsinput   (doc 1207)
  //   - image / images / *image(s)                  → faupload    (doc 177/183)
  //   - enum                                       → <select>
  //   - set                                        → <select multiple>
  //   - text                                       → <textarea>
  //   - NOT NULL columns                           → data-rule="required"
  const inputs: string[] = []
  for (const c of table.columns) {
    if (c.isPk) continue
    if (['createtime', 'updatetime', 'deletetime'].includes(c.name)) continue
    const label = c.comment || c.name
    const required = !c.nullable && (c.default === null || c.default === undefined)
    const ruleAttr = required ? ' data-rule="required"' : ''
    const wrap = (inner: string): string =>
      `'<div class="form-group"><label class="col-xs-2 control-label">${escapeForTemplate(label)}</label><div class="col-xs-8">${escapeForTemplate(inner)}</div></div>'`

    // SelectPage — relation id columns (doc 178).
    if (/^(category|user|admin)_ids?$/.test(c.name)) {
      const rel = c.name.startsWith('category') ? 'category' : c.name.startsWith('user') ? 'user/user' : 'auth/admin'
      const multi = c.name.endsWith('_ids') ? ' data-multiple="true"' : ''
      inputs.push(wrap(`<input class="form-control selectpage" data-source="${rel}/selectpage"${multi}${ruleAttr} name="row[${c.name}]" type="text" value="">`))
    // city-picker (doc 180).
    } else if (c.name === 'city' || /city$/.test(c.name)) {
      inputs.push(wrap(`<input class="form-control" data-toggle="city-picker"${ruleAttr} name="row[${c.name}]" type="text" value="">`))
    // datetimerange — *range columns (doc 736). Checked before datetimepicker.
    } else if (/range$/.test(c.name)) {
      inputs.push(wrap(`<input class="form-control datetimerange"${ruleAttr} name="row[${c.name}]" type="text" value="">`))
    // datetimepicker — date/datetime/timestamp columns or *time suffix (doc 181).
    } else if (['date', 'datetime', 'timestamp'].includes(c.dataType) || /time$/.test(c.name)) {
      const fmt = c.dataType === 'date' ? 'YYYY-MM-DD' : 'YYYY-MM-DD HH:mm:ss'
      inputs.push(wrap(`<input class="form-control datetimepicker" data-date-format="${fmt}"${ruleAttr} name="row[${c.name}]" type="text" value="">`))
    // switcher — `switch` column or tinyint(1) (doc 185).
    } else if (c.name === 'switch' || c.dataType === 'tinyint') {
      inputs.push(wrap(`<input id="c-${c.name}" name="row[${c.name}]" type="hidden" value="1"><a href="javascript:;" data-toggle="switcher" class="btn-switcher" data-input-id="c-${c.name}" data-yes="1" data-no="0"><i class="fa fa-toggle-on text-success fa-2x"></i></a>`))
    // tagsinput — `tags` column or *tags suffix (doc 1207).
    } else if (c.name === 'tags' || /tags$/.test(c.name)) {
      inputs.push(wrap(`<input class="form-control" data-role="tagsinput"${ruleAttr} name="row[${c.name}]" type="text" value="">`))
    // faupload — image / file columns (doc 177 / 183).
    } else if (/images?$/.test(c.name) || /file$/.test(c.name)) {
      const isMulti = /images$/.test(c.name)
      const mime = /images?$/.test(c.name) ? 'image/*' : '*'
      inputs.push(wrap(
        `<div class="input-group">` +
        `<input id="c-${c.name}" class="form-control"${ruleAttr} name="row[${c.name}]" type="text" value="">` +
        `<div class="input-group-addon no-border no-padding">` +
        `<span><button type="button" class="btn btn-danger faupload" data-input-id="c-${c.name}" data-mimetype="${mime}" data-multiple="${isMulti}"><i class="fa fa-upload"></i> 上传</button></span> ` +
        `<span><button type="button" class="btn btn-primary fachoose" data-input-id="c-${c.name}" data-mimetype="${mime}" data-multiple="${isMulti}"><i class="fa fa-list"></i> 选择</button></span>` +
        `</div></div>`,
      ))
    // fieldlist — dynamic key/value list editor for array / json columns
    // (doc 1206). The hidden <textarea> holds the JSON; require-form.js'
    // default `fieldlisttpl` renders the key+value rows + add/remove buttons.
    } else if (c.name === 'array' || c.name === 'json' || /json$/.test(c.name) || /array$/.test(c.name)) {
      inputs.push(wrap(
        `<dl class="fieldlist" data-name="row[${c.name}]">` +
        `<dd><a href="javascript:;" class="btn btn-sm btn-success btn-append"><i class="fa fa-plus"></i> 追加</a></dd>` +
        `<textarea name="row[${c.name}]" class="form-control hide" rows="5"></textarea>` +
        `</dl>`,
      ))
    // select (selectpicker / bootstrap-select, doc 185) — enum, set, or any
    // column whose comment carries a `key=value,key=value` option map. A
    // `(多选)` marker (or the set type) makes it a multi-select.
    } else if (c.enumValues || parseOptionComment(c.comment ?? '')) {
      const parsed = parseOptionComment(c.comment ?? '')
      const multiple = c.dataType === 'set' || (parsed?.multiple ?? false)
      const options: Array<[string, string]> = parsed
        ? parsed.options
        : (c.enumValues ?? []).map((v) => [v, v] as [string, string])
      const optsHtml = options.map(([k, v]) => `<option value="${k}">${v}</option>`).join('')
      const nm = multiple ? `row[${c.name}][]` : `row[${c.name}]`
      const mult = multiple ? ' multiple' : ''
      inputs.push(wrap(`<select class="form-control selectpicker"${mult}${ruleAttr} name="${nm}">${optsHtml}</select>`))
    // text → textarea. A `content` column additionally gets the `editor`
    // class — FastAdmin's `php think crud` editorfield default — so an
    // installed rich-text editor addon hooks it; without one it degrades to a
    // plain textarea, exactly like FastAdmin core.
    } else if (['text', 'longtext', 'mediumtext'].includes(c.dataType)) {
      const cls = c.name === 'content' ? 'form-control editor' : 'form-control'
      inputs.push(wrap(`<textarea class="${cls}"${ruleAttr} name="row[${c.name}]"></textarea>`))
    // default — number / text input.
    } else {
      const type = ['int', 'bigint', 'decimal', 'float', 'double'].includes(c.dataType) ? 'number' : 'text'
      inputs.push(wrap(`<input class="form-control" type="${type}"${ruleAttr} name="row[${c.name}]" value="">`))
    }
  }
  return inputs.join(' +\n        ') || `''`
}

function escapeForTemplate(s: string): string {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

/**
 * Parse a FastAdmin-style column comment into a field label + option map.
 * FastAdmin's CRUD writes comments like `状态值:0=禁用,1=正常,2=推荐` or
 * `性别(单选):male=男,female=女` — the text before the first `:` is the field
 * label, the rest is a `key=value,key=value` choice map. Returns null when the
 * comment carries no clean option map (so the column is not a <select>).
 * A `多选` marker anywhere in the comment flags a multi-select.
 */
function parseOptionComment(
  comment: string,
): { label: string; multiple: boolean; options: Array<[string, string]> } | null {
  const idx = (comment || '').indexOf(':')
  if (idx < 0) return null
  const label = comment.slice(0, idx).trim()
  const options: Array<[string, string]> = []
  for (const pair of comment.slice(idx + 1).split(',')) {
    const eq = pair.indexOf('=')
    if (eq <= 0) return null // not a clean k=v map — treat as a plain comment
    options.push([pair.slice(0, eq).trim(), pair.slice(eq + 1).trim()])
  }
  if (options.length === 0) return null
  return { label, multiple: /多选/.test(comment), options }
}
